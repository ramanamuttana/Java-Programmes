package decisionlopps;

public class EvenandOddNumbers {

	// print the even and odd numbers in a given range
	public static void main(String[] args) {

		for (int i = 0; i < 50; i++) {
			if (i % 2 == 0) {
				System.out.println(i);
			}else{
				System.out.println(i);
			}
		}
	}
}
